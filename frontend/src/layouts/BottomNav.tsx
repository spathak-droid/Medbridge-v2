import { useEffect, useState } from 'react'
import { NavLink } from 'react-router-dom'
import { useAuth } from '../contexts/AuthContext'
import { usePatient } from '../hooks/usePatient'
import { getSchedule, getUnreadCount } from '../lib/api'

const patientItems = [
  { to: '/', label: 'Chat', icon: ChatIcon, badgeType: null as string | null },
  { to: '/program', label: 'Program', icon: ProgramIcon, badgeType: null as string | null },
  { to: '/messages', label: 'Messages', icon: MessagesIcon, badgeType: 'messages' as string | null },
  { to: '/reminders', label: 'Reminders', icon: ReminderIcon, badgeType: 'reminders' as string | null },
  { to: '/progress', label: 'Progress', icon: ProgressIcon, badgeType: null as string | null },
]

const clinicianItems = [
  { to: '/dashboard', label: 'Dashboard', icon: DashboardIcon, badgeType: null as string | null },
  { to: '/analytics', label: 'Analytics', icon: AnalyticsIcon, badgeType: null as string | null },
  { to: '/settings', label: 'Settings', icon: SettingsIcon, badgeType: null as string | null },
]

export function BottomNav() {
  const { user } = useAuth()
  const { patientId } = usePatient()
  const isPatient = user?.role === 'patient'
  const items = isPatient ? patientItems : clinicianItems
  const [unreadCount, setUnreadCount] = useState(0)
  const [reminderCount, setReminderCount] = useState(0)

  useEffect(() => {
    if (!isPatient || !patientId) return
    const fetchCounts = () => {
      getUnreadCount(patientId).then((data) => setUnreadCount(data.count)).catch(() => {})
      getSchedule(patientId).then((events) => setReminderCount(events.filter((e) => e.status === 'PENDING').length)).catch(() => {})
    }
    fetchCounts()
    const interval = setInterval(fetchCounts, 30000)
    return () => clearInterval(interval)
  }, [isPatient, patientId])

  return (
    <nav className="
      lg:hidden
      fixed bottom-0 left-0 right-0
      flex items-center justify-around
      bg-white/90 backdrop-blur-lg
      border-t border-neutral-200/60
      px-2 pb-[env(safe-area-inset-bottom)] pt-1
      z-40
    ">
      {items.map(({ to, label, icon: Icon, badgeType }) => {
        const count = badgeType === 'messages' ? unreadCount : badgeType === 'reminders' ? reminderCount : 0
        const badgeColor = badgeType === 'reminders' ? 'bg-amber-500' : 'bg-red-500'
        return (
          <NavLink
            key={to}
            to={to}
            className={({ isActive }) => `
              flex flex-col items-center gap-0.5
              px-2 py-2
              text-[10px] font-medium
              transition-colors
              min-w-[48px]
              relative
              ${isActive ? 'text-primary-600' : 'text-neutral-400'}
            `}
          >
            <div className="relative">
              <Icon className="w-5 h-5" />
              {count > 0 && (
                <span className={`absolute -top-1.5 -right-2 min-w-[16px] h-4 px-1 rounded-full ${badgeColor} text-white text-[9px] font-bold flex items-center justify-center`}>
                  {count > 9 ? '9+' : count}
                </span>
              )}
            </div>
            {label}
          </NavLink>
        )
      })}
    </nav>
  )
}

