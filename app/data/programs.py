def get_program_for_patient(external_id: str, program_type: str | None = None) -> dict | None:
    """Look up the exercise program assigned to a patient.

    Args:
        external_id: Patient external ID (used as fallback for demo patients).
        program_type: The patient's assigned program_type from DB (preferred).

    Returns the full program dict, or None if no program is assigned.
    """
    # Prefer the DB-stored program_type
    if program_type:
        return PROGRAMS.get(program_type)
    # Fallback to hardcoded map for demo patients
    program_key = PATIENT_PROGRAM_MAP.get(external_id)
    if program_key:
        return PROGRAMS.get(program_key)
    return None


def get_available_programs() -> list[dict[str, str]]:
    """Return a summary of all available programs for the LLM to choose from."""
    return [
        {"key": key, "name": prog["program_name"]}
        for key, prog in PROGRAMS.items()
    ]
